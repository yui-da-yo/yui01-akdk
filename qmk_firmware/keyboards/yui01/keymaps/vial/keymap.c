// マウスの動きを検知してVialのオートマウス機能に伝える処理
#ifdef POINTING_DEVICE_AUTO_MOUSE_ENABLE
report_mouse_t pointing_device_task_user(report_mouse_t mouse_report) {
    // --- 追加: スクロールモードがONの場合、動きをホイール回転に変換 ---
    if (is_scroll_mode) {
        mouse_report.h = mouse_report.x; // 左右
        mouse_report.v = -mouse_report.y; // 上下（反転）
        mouse_report.x = 0;
        mouse_report.y = 0;
    }

    // 元々のオートマウス処理（変更なし）
    if (mouse_report.x != 0 || mouse_report.y != 0) {
        set_auto_mouse_enable(true);
    }
    return mouse_report;
}
#endif

void pointing_device_init_user(void) {
    pointing_device_set_cpi(500);
}

// --- 追加: User 0キーでスクロールモードを切り替える処理 ---
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case VIAL_USER00: // Vialの「User 0」に相当
            if (record->event.pressed) {
                is_scroll_mode = !is_scroll_mode; // 押すたびにON/OFF切り替え
            }
            return false;
    }
    return true;
}
